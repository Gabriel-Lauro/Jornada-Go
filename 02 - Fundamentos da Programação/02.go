/*
Escreva expressões utilizando os operadores, e atribua seus valores a variáveis.
==
!=
<=
<
>=
>

Demonstre estes valores.
*/

package main

import "fmt"

func main() {

	a := (10 == 20)
	b := (10 != 20)
	c := (10 <= 20)
	d := (10 < 20)
	e := (10 >= 20)
	f := (10 > 20)

	fmt.Printf("A = %v\n", a)
	fmt.Printf("B = %v\n", b)
	fmt.Printf("C = %v\n", c)
	fmt.Printf("D = %v\n", d)
	fmt.Printf("E = %v\n", e)
	fmt.Printf("F = %v\n", f)

}
